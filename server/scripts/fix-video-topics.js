/**
 * fix-video-topics.js — set each question's topic from its set-VIDEO's title.
 *
 * Every videoScope:"set" question carries the intro video of the batch it was
 * generated in, so the video's YouTube TITLE is the ground-truth topic for that
 * whole batch (checked via oEmbed). This assigns every question sharing a video
 * to one title-derived topic, which fixes videos showing under the wrong topic
 * AND corrects content-classifier mistakes. No-video questions keep their
 * content-classified topic. Updates data/cbt/* + manifest + the mapping. No
 * Firestore.
 *
 *   node server/scripts/fix-video-topics.js
 */
const fs = require("fs");
