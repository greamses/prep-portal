const vidId = (url) => String(url || "").replace(/.*youtu\.be\/([^?]+).*/, "$1");

function readArr(file, name) { return JSON.parse(fs.readFileSync(file, "utf8").match(new RegExp(`export const ${name} = ([\\s\\S]*);\\s*$`))[1]); }
function writeModule(file, name, value) { fs.writeFileSync(file, `// AUTO-GENERATED by server/scripts/export-cbt-bank.js — do not edit by hand.\nexport const ${name} = ${JSON.stringify(value)};\n`); }
function topicSummary(arr) {
  const by = {}; arr.forEach((q) => { const t = q.topic || "General"; by[t] = (by[t] || 0) + 1; });
  return Object.entries(by).map(([name, count]) => ({ name, count, papers: Math.ceil(count / PAPER_SIZE) })).sort((a, b) => a.name.localeCompare(b.name));
}

const mapData = JSON.parse(fs.readFileSync(MAP, "utf8"));
const newTopicById = new Map();
const classMap = {};
let changed = 0, unknown = new Set();

for (const f of fs.readdirSync(C_DIR).filter((x) => x.endsWith(".js"))) {
  const file = path.join(C_DIR, f);
  const arr = readArr(file, "QUESTIONS");
  for (const q of arr) {
    if (q.videoScope === "set" && q.video) {
      const id = vidId(q.video);
      const topic = VIDEO_TOPIC[id];
      if (topic) { if (q.topic !== topic) { q.topic = topic; changed++; } }
      else unknown.add(id);
    }
    newTopicById.set(q.id, q.topic); // record final topic (video-set or kept content)
  }
  writeModule(file, "QUESTIONS", arr);
  const [cls, subject] = f.replace(/\.js$/, "").split("__");
  (classMap[cls] ||= []).push({ key: subject, label: (arr[0] && arr[0].subjectLabel) || subject, count: arr.length, topics: topicSummary(arr) });
}

// rebuild manifest.classes; keep exams + paperSize
const m = readArr(MANIFEST, "MANIFEST");
m.classes = Object.keys(classMap).sort((a, b) => classNum(a) - classNum(b)).map((c) => ({
  key: c, label: CLASS_LABELS[c] || c, subjects: classMap[c].sort((a, b) => a.label.localeCompare(b.label)),
}));
writeModule(MANIFEST, "MANIFEST", m);

// keep the mapping json in sync for the later Firestore apply
for (const a of mapData.assignments) { if (newTopicById.has(a.id)) a.newTopic = newTopicById.get(a.id); }
mapData.classChanges = [];
fs.writeFileSync(MAP, JSON.stringify(mapData, null, 2));

console.log(`Set topics from video titles: ${changed} questions changed.`);
if (unknown.size) console.log(`WARNING: videos with no title mapping: ${[...unknown].join(", ")}`);
console.log("Topics per bucket now:");
for (const c of m.classes) for (const s of c.subjects) {
  console.log(`  ${c.key}/${s.key}: ${s.topics.map((t) => `${t.name}(${t.count})`).join(", ")}`);
}
