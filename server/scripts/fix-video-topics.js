const ROOT = path.join(__dirname, "..", "..");
const C_DIR = path.join(ROOT, "data", "cbt", "c");
const MANIFEST = path.join(ROOT, "data", "cbt", "manifest.js");
const MAP = path.join(__dirname, "cbt-reclassify.json");
const PAPER_SIZE = 60;
const CLASS_LABELS = {};
for (let i = 1; i <= 12; i++) CLASS_LABELS["grade" + i] = "Grade " + i;
const classNum = (k) => parseInt(String(k).replace("grade", ""), 10) || 0;

// videoId → topic, derived from the YouTube title (oEmbed) of each set-video.
