/**
 * apply-static-reclassify.js — apply the reviewed topic classification
 * (cbt-reclassify.json) directly to the STATIC bank in data/cbt/, with NO
 * Firestore. Since student practice is served from these files, this makes the
 * new topics live on the next deploy without spending any read/write quota.
 *
 * It rewrites each data/cbt/c/<class>__<subject>.js (updates every question's
 * `topic`) and rebuilds data/cbt/manifest.js's `classes` section (the exam
 * tabs + paperSize are left untouched). Grades are NOT changed.
 *
 * NOTE: Firestore still holds the old "General" topics. Run
 * apply-cbt-reclassify.js after the quota resets so a future export doesn't
 * revert this. Idempotent.
 *
 *   node server/scripts/apply-static-reclassify.js
 */
const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..", "..");
const C_DIR = path.join(ROOT, "data", "cbt", "c");
const MANIFEST = path.join(ROOT, "data", "cbt", "manifest.js");
const MAP = path.join(__dirname, "cbt-reclassify.json");
const PAPER_SIZE = 60;

const CLASS_LABELS = {};
for (let i = 1; i <= 12; i++) CLASS_LABELS["grade" + i] = "Grade " + i;
const classNum = (k) => parseInt(String(k).replace("grade", ""), 10) || 0;

function readArray(file, name) {
  const txt = fs.readFileSync(file, "utf8");
  const m = txt.match(new RegExp(`export const ${name} = ([\\s\\S]*);\\s*$`));
  return JSON.parse(m[1]);
}
function writeModule(file, name, value) {
  fs.writeFileSync(file, `// AUTO-GENERATED by server/scripts/export-cbt-bank.js — do not edit by hand.\nexport const ${name} = ${JSON.stringify(value)};\n`);
}
// topic → {name,count,papers}, sorted by name (mirrors export-cbt-bank.js).
function topicSummary(arr) {
  const by = {};
  arr.forEach((q) => { const t = q.topic || "General"; by[t] = (by[t] || 0) + 1; });
  return Object.entries(by).map(([name, count]) => ({ name, count, papers: Math.ceil(count / PAPER_SIZE) }))
    .sort((a, b) => a.name.localeCompare(b.name));
}

// id → newTopic
const map = new Map();
JSON.parse(fs.readFileSync(MAP, "utf8")).assignments.forEach((a) => map.set(a.id, a.newTopic));
console.log(`Loaded ${map.size} topic assignments.`);

// 1) Rewrite each class bucket file with updated topics.
const classMap = {}; // class -> [{key,label,count,topics}]
let touched = 0, changed = 0;
for (const f of fs.readdirSync(C_DIR).filter((x) => x.endsWith(".js"))) {
  const file = path.join(C_DIR, f);
  const arr = readArray(file, "QUESTIONS");
  let fileChanged = false;
  for (const q of arr) {
    const nt = map.get(q.id);
    if (nt && nt !== q.topic) { q.topic = nt; changed++; fileChanged = true; }
  }
  if (fileChanged) { writeModule(file, "QUESTIONS", arr); touched++; }
  const [cls, subject] = f.replace(/\.js$/, "").split("__");
  (classMap[cls] ||= []).push({
    key: subject, label: (arr[0] && arr[0].subjectLabel) || subject,
    count: arr.length, topics: topicSummary(arr),
  });
}
console.log(`Updated ${changed} question topics across ${touched} bucket files.`);

// 2) Rebuild manifest.classes from the updated buckets; keep exams + paperSize.
const m = readArray(MANIFEST, "MANIFEST");
m.classes = Object.keys(classMap).sort((a, b) => classNum(a) - classNum(b)).map((c) => ({
  key: c, label: CLASS_LABELS[c] || c,
  subjects: classMap[c].sort((a, b) => a.label.localeCompare(b.label)),
}));
writeModule(MANIFEST, "MANIFEST", m);

const totalTopics = new Set();
m.classes.forEach((c) => c.subjects.forEach((s) => s.topics.forEach((t) => totalTopics.add(c.key + "|" + s.key + "|" + t.name))));
console.log(`Rebuilt manifest: ${m.classes.length} classes, ${totalTopics.size} class+subject+topic groups.`);
console.log("Done. Commit data/cbt + deploy to publish to students.");
