/**
 * export-cbt-bank.js — materialise the student-visible CBT bank as STATIC JS
 * files under data/cbt/, so practice is served from the repo (≈0 Firestore
 * reads). Firestore stays the source of truth + runtime fallback.
 *
 * Output (ES modules, dynamic-import'd by the picker + quiz):
 *   data/cbt/manifest.js              — navigation index (no question bodies):
 *       classes[]  → subject → topic → {count, papers}      (the Class tab)
 *       exams.national[] / exams.international[] → same       (the exam tabs)
 *   data/cbt/c/<class>__<subject>.js  — questions for a class+subject bucket
 *   data/cbt/e/<scheme>__<subject>.js — questions for an exam+subject bucket
 *
 * Each bucket array is pre-sorted (createdAt asc) so the client can slice
 * papers of 60 deterministically (paper 1 = items 0–59, …).
 *
 * Resilient: on any failure it logs and exits 0 WITHOUT touching existing files,
 * so a flaky build never ships an empty bank or breaks the deploy.
 *
 *   node server/scripts/export-cbt-bank.js
 */

const fs = require("fs");
const path = require("path");

const PAPER_SIZE = 60;
const OUT_DIR = path.join(__dirname, "..", "..", "data", "cbt");

// ── Labels (mirror server/routes/cbt.js) ───────────────────────────────────
const CLASS_LABELS = {
  primary1: "Primary 1", primary2: "Primary 2", primary3: "Primary 3",
  primary4: "Primary 4", primary5: "Primary 5", primary6: "Primary 6",
  jss1: "JSS1", jss2: "JSS2", jss3: "JSS3",
  sss1: "SSS1", sss2: "SSS2", sss3: "SSS3",
  grade1: "Grade 1", grade2: "Grade 2", grade3: "Grade 3", grade4: "Grade 4", grade5: "Grade 5",
  grade6: "Grade 6", grade7: "Grade 7", grade8: "Grade 8",
  grade9: "Grade 9", grade10: "Grade 10", grade11: "Grade 11", grade12: "Grade 12",
};
const CLASS_ORDER = Object.keys(CLASS_LABELS);
const SUBJECT_LABELS = {
  english: "English Language", mathematics: "Mathematics", biology: "Biology",
  physics: "Physics", chemistry: "Chemistry", economics: "Economics",
  government: "Government", commerce: "Commerce", geography: "Geography",
  crk: "Christian Religious Studies", irk: "Islamic Religious Studies",
  englishlit: "Literature in English", accounting: "Financial Accounting",
  history: "History", civiledu: "Civic Education", agric: "Agricultural Science",
  quantitative: "Quantitative Reasoning", verbal: "Verbal Reasoning",
  generalstudies: "General Studies", furthermaths: "Further Mathematics",
  ict: "ICT", businessstudies: "Business Studies",
};
// Exam styles, split into the National + International tabs.
const EXAMS = {
  national: {
    utme: "UTME-style", wassce: "WASSCE-style", postutme: "Post-UTME style", cee: "Common Entrance style",
  },
  international: { sat: "SAT-style", igcse: "IGCSE-style", alevel: "A-Level-style" },
};
const EXAM_OF = {}; // scheme key → region
for (const region of Object.keys(EXAMS)) for (const k of Object.keys(EXAMS[region])) EXAM_OF[k] = region;

const subjLabel = (k) => SUBJECT_LABELS[k] || (k ? k[0].toUpperCase() + k.slice(1) : "Subject");
const ms = (x) => (x && x.toMillis ? x.toMillis() : (typeof x === "number" ? x : 0));
const sanitize = (s) => String(s || "").toLowerCase().replace(/[^a-z0-9]/g, "");

// Slim a Firestore doc to just the fields the player needs.
function slim(id, x) {
  return {
    id, type: x.type || "objective", question: x.question,
    options: x.options || null, answerIndex: typeof x.answerIndex === "number" ? x.answerIndex : null,
    answer: x.answer || null, explanation: x.explanation || "", hint: x.hint || "",
    image: x.image || null, video: x.video || null, videoScope: x.videoScope || "question",
    topic: x.topic || "General", classLevel: x.classLevel || null,
    scheme: x.scheme || null, subject: x.subject, subjectLabel: x.subjectLabel || subjLabel(x.subject), grade: x.grade || null,
  };
}

// topic → {count, papers}, sorted, for the manifest.
function topicSummary(arr) {
  const byTopic = {};
  arr.forEach((q) => { const t = q.topic || "General"; byTopic[t] = (byTopic[t] || 0) + 1; });
  return Object.entries(byTopic)
    .map(([name, count]) => ({ name, count, papers: Math.ceil(count / PAPER_SIZE) }))
    .sort((a, b) => a.name.localeCompare(b.name));
}

function writeModule(file, exportName, value) {
  const dir = path.dirname(file);
  fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(file, `// AUTO-GENERATED by server/scripts/export-cbt-bank.js — do not edit by hand.\nexport const ${exportName} = ${JSON.stringify(value)};\n`);
}

(async () => {
  const admin = require("firebase-admin");
  let credential;
  if (process.env.FIREBASE_SERVICE_ACCOUNT) credential = admin.credential.cert(JSON.parse(process.env.FIREBASE_SERVICE_ACCOUNT));
  else credential = admin.credential.cert(require("../serviceAccountKey.json"));
  if (!admin.apps.length) admin.initializeApp({ credential });
  const db = admin.firestore();

  // Respect the hideOriginals kill-switch: when on, exclude verbatim past papers.
  let hideOriginals = true;
  try { const s = await db.collection("config").doc("site").get(); hideOriginals = !(s.exists && s.data().hideOriginals === false); } catch (_) {}

  const snap = await db.collection("cbtQuestions").get();
  const classBuckets = {}; // `${class}__${subject}` → [slim]
  const examBuckets = {};   // `${scheme}__${subject}` → [slim]
  const order = {};         // doc id → createdAt millis (for stable sort)

  snap.forEach((d) => {
    const x = d.data();
    if (!x.subject) return;
    // Student-visible only: drop verbatim past originals when the switch is on.
    if (hideOriginals && x.source === "past") return;
    const q = slim(d.id, x);
    order[d.id] = ms(x.createdAt);
    // Class tab: real classes only (never the "unsorted" holding pen).
    if (x.classLevel && x.classLevel !== "unsorted" && CLASS_LABELS[x.classLevel]) {
      (classBuckets[`${x.classLevel}__${x.subject}`] ||= []).push(q);
    }
    // Exam tabs: only national/international exam schemes.
    if (x.scheme && EXAM_OF[x.scheme]) {
      (examBuckets[`${x.scheme}__${x.subject}`] ||= []).push(q);
    }
  });

  const sortBucket = (arr) => arr.sort((a, b) => (order[a.id] || 0) - (order[b.id] || 0));

  // Fresh output dir (drop stale buckets).
  fs.rmSync(OUT_DIR, { recursive: true, force: true });
  fs.mkdirSync(OUT_DIR, { recursive: true });

  // ── Class buckets + manifest.classes ──
  const classMap = {}; // class → [{subject}]
  for (const key of Object.keys(classBuckets)) {
    const [cls, subject] = key.split("__");
    const arr = sortBucket(classBuckets[key]);
    writeModule(path.join(OUT_DIR, "c", `${cls}__${subject}.js`), "QUESTIONS", arr);
    (classMap[cls] ||= []).push({ key: subject, label: subjLabel(subject), count: arr.length, topics: topicSummary(arr) });
  }
  const classes = CLASS_ORDER.filter((c) => classMap[c]).map((c) => ({
    key: c, label: CLASS_LABELS[c],
    subjects: classMap[c].sort((a, b) => a.label.localeCompare(b.label)),
  }));

  // ── Exam buckets + manifest.exams (national / international) ──
  const examMap = {}; // scheme → [{subject}]
  for (const key of Object.keys(examBuckets)) {
    const [scheme, subject] = key.split("__");
    const arr = sortBucket(examBuckets[key]);
    writeModule(path.join(OUT_DIR, "e", `${scheme}__${subject}.js`), "QUESTIONS", arr);
    (examMap[scheme] ||= []).push({ key: subject, label: subjLabel(subject), count: arr.length, topics: topicSummary(arr) });
  }
  const examsByRegion = { national: [], international: [] };
  for (const region of Object.keys(EXAMS)) {
    for (const scheme of Object.keys(EXAMS[region])) {
      if (!examMap[scheme]) continue;
      examsByRegion[region].push({
        key: scheme, label: EXAMS[region][scheme],
        subjects: examMap[scheme].sort((a, b) => a.label.localeCompare(b.label)),
      });
    }
  }

  writeModule(path.join(OUT_DIR, "manifest.js"), "MANIFEST", {
    paperSize: PAPER_SIZE,
    classes,
    exams: examsByRegion,
  });

  const nClass = Object.keys(classBuckets).length, nExam = Object.keys(examBuckets).length;
  const totalQ = Object.values(classBuckets).reduce((n, a) => n + a.length, 0);
  console.log(`Exported ${totalQ} class-question slots across ${nClass} class buckets + ${nExam} exam buckets → data/cbt/`);
  console.log(`Classes: ${classes.length} · National exams: ${examsByRegion.national.length} · International exams: ${examsByRegion.international.length}`);
  process.exit(0);
})().catch((e) => {
  console.error("export-cbt-bank failed (keeping existing files):", e.message);
  process.exit(0); // never fail the build
});
